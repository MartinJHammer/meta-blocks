export const multiply = (...numbers: number[]): number => numbers.reduce((a, b) => a * b);
export const add = (...numbers: number[]): number => numbers.reduce((a, b) => a + b);
export const subtract = (...numbers: number[]): number => numbers.reduce((a, b) => a - b);
export const divide = (...numbers: number[]): number => numbers.reduce((a, b) => a / b);
